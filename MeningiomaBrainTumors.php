                        <p class="card-text">Headaches</p>
                        <p class="card-text">Seizures</p>
                        <p class="card-text">Blurred vision</p>
                        <p class="card-text">Weakness in the arms or legs</p>
                        <p class="card-text">Numbness</p>
                        <p class="card-text">Speech difficulties</p>


                        <h5 class="card-title blogH5">HOW IS A MENINGIOMA DIAGNOSED?</h5>
                        <p class="card-text">The diagnosis of a meningioma rarely occurs before symptoms are noticeable.
                            If you have symptoms, the first thing a doctor may order is a brain scan, either an MRI or a
                            CT scan. With these scans, a doctor will be able to identify a tumor and its size. </p>
                        <h5 class="card-title blogH5">WHAT ARE THE TREATMENT OPTIONS FOR MENINGIOMA TUMORS?</h5>
                        <p class="card-text">First, if the tumor has not resulted in any significant symptoms, doctor
                            may recommend close observation with regular brain scans to monitor the tumor’s growth.
                            Should the tumor begin to cause problems or continue to grow, surgery may be required.</p>
                        <p class="card-text">The type of surgery to remove the tumor would be a craniotomy. This
                            involves removing a piece of bone from the skull, which allows the neurosurgeon access to
                            the area of the brain where the tumor is located. Then the tumor is removed, and the bone
                            put back in its place. The position of the tumor has much to do with how the surgeon can
                            access the brain.</p>
                        <p class="card-text">Sometimes because of its location, surgery isn’t possible, so radiation
                            therapy is used instead to help shrink the tumor and prevent it from growing larger. If it’s
                            malignant, then radiation will also kill the cancer cells.</p>

