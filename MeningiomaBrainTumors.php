                        <h5 class="card-title blogH5">WHAT ARE MENINGIOMA BRAIN TUMORS?</h5>
                        <p class="card-text">This type of tumor forms on the membranes of the brain and spinal cord,
                            inside the skull. The membranes it grows on are called meninges, hence its name. Meningioma
                            occurrences are the most common central nervous system tumors. They affect more women than
                            men. While most tumors are classified as benign or malignant, some meningiomas are atypical,
                            which indicates they could turn into cancer. Only a small percentage are cancerous. Those
                            that are, however, do grow fast. They can then spread to other areas of the body, like the
                            lungs.</p>

                        <p class="card-text">The causes of these tumors are not completely known, however, there are
                            risk factors, including exposure to radiation and Neurofibromatosis type 2, a genetic
                            disorder. Additional research has seen a possible correlation between previous injury and
                            meningiomas. Other research suggests a link between the hormone progesterone and the
                            development of these tumors.</p>
